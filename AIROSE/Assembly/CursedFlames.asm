;==============================================================================
; WORLD MODEL – ASCII representation of Earth
; A simple geographical model using characters.
; Assembled with NASM for Linux x86-64.
;
; Build: nasm -f elf64 earth_model.asm -o earth_model.o
;        ld earth_model.o -o earth_model
; Run:   ./earth_model
;==============================================================================

section .data
    ; Header
    header db 27, '[36m', 0          ; Cyan color
           db '╔════════════════════════════════════════════════════════════════════════════╗', 10
           db '║                           EARTH MODEL (simplified)                        ║', 10
           db '║                         # land  · ocean  * ice                             ║', 10
           db '╚════════════════════════════════════════════════════════════════════════════╝', 10
           db 27, '[0m', 0            ; Reset color
    header_len equ $ - header - 1     ; exclude final null

    ; Earth map: 80x24 grid
    ; Each line is 80 characters, followed by newline.
    ; The map is a rough approximation of continents.
    map db '................................................................................', 10
        db '................................................................................', 10
        db '...........................................###................................', 10
        db '.........................................########..............................', 10
        db '........................................##########.............................', 10
        db '.......................................############............................', 10
        db '......................................##############...........................', 10
        db '.....................................################..........................', 10
        db '....................................##################.........................', 10
        db '...................................####################........................', 10
        db '..................................######################.......................', 10
        db '.................................########################......................', 10
        db '................................##########################.....................', 10
        db '...............................############################....................', 10
        db '..............................##############################...................', 10
        db '.............................################################..................', 10
        db '............................##################################.................', 10
        db '...........................####################################................', 10
        db '..........................######################################...............', 10
        db '.........................########################################..............', 10
        db '........................##########################################.............', 10
        db '.......................############################################............', 10
        db '................................................................................', 10
        db '................................................................................', 10
    map_len equ $ - map

section .text
    global _start

_start:
    ; Print header
    mov eax, 1                  ; sys_write
    mov edi, 1                  ; stdout
    mov rsi, header
    mov edx, header_len
    syscall

    ; Print the map
    mov eax, 1
    mov edi, 1
    mov rsi, map
    mov edx, map_len
    syscall

    ; Exit
    mov eax, 60                 ; sys_exit
    xor edi, edi
    syscall